function video()
    {
        if(document.getElementById('video').className == 'videoClose')
            {
            document.getElementById('video').className = 'videoOpen';
            document.getElementById('portraid').className = 'portraidClose';
            } else {
                document.getElementById('video').className = 'videoClose';
                document.getElementById('portraid').className = 'Portraid';
                    }
    } 

